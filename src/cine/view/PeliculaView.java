    private void btnInsPeliculaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInsPeliculaMouseClicked
        // TODO add your handling code here:
        if(txtId.getText().equals("") || txtTitulo.getText().equals("") || txtGenero.getText().equals("") || txtClasificacion.getText().equals("") || txtFormato.getText().equals("") || txtADescripcion.getText().equals("")){
        
            JOptionPane.showMessageDialog(null, "Llene todos los campos", "Advertencia",  JOptionPane.WARNING_MESSAGE);
        
        }else{
            Pelicula nuevoCine = new Pelicula(txtId.getText(), txtTitulo.getText(), txtGenero.getText(), txtClasificacion.getText(), txtFormato.getText(), txtADescripcion.getText());
            this.listaPelicula.add(nuevoCine);
            addRowToJTable();
            borrarCamposCine();
        }
        
        
    }//GEN-LAST:event_btnInsPeliculaMouseClicked
    
    private void borrarCamposCine(){
        
        txtId.setText(""); 
        txtTitulo.setText(""); 
        txtGenero.setText("");
        txtClasificacion.setText("");
        txtFormato.setText("");
        txtADescripcion.setText("");
    
    }
    
    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        this.selectedRow = jTable2.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        txtId.setText(model.getValueAt(this.selectedRow, 0).toString()); 
        txtTitulo.setText(model.getValueAt(this.selectedRow, 1).toString()); 
        txtGenero.setText(model.getValueAt(this.selectedRow, 2).toString());
        txtClasificacion.setText(model.getValueAt(this.selectedRow, 3).toString());
        txtFormato.setText(model.getValueAt(this.selectedRow, 4).toString());
        txtADescripcion.setText(model.getValueAt(this.selectedRow, 5).toString());
        
    }//GEN-LAST:event_jTable2MouseClicked

    private void btnUpdPeliculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdPeliculaActionPerformed
        // TODO add your handling code here:
        if(this.selectedRow == -1){
            JOptionPane.showMessageDialog(null, "Seleccione en la tabla el cine", "Advertencia",  JOptionPane.WARNING_MESSAGE);
        }else{
            Pelicula nuevoCine = new Pelicula(txtId.getText(), txtTitulo.getText(), txtGenero.getText(), txtClasificacion.getText(), txtFormato.getText(), txtADescripcion.getText());
            this.listaPelicula.set(this.selectedRow, nuevoCine);
            addRowToJTable();
        }
    }//GEN-LAST:event_btnUpdPeliculaActionPerformed

    private void btnDelPeliculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelPeliculaActionPerformed
        // TODO add your handling code here:
        if(this.selectedRow == -1){
            JOptionPane.showMessageDialog(null, "Seleccione en la tabla el cine", "Advertencia",  JOptionPane.WARNING_MESSAGE);
        }
        else{
            this.listaPelicula.remove(this.selectedRow);
            addRowToJTable();
            this.selectedRow = -1;
            borrarCamposCine();
        } 
    }//GEN-LAST:event_btnDelPeliculaActionPerformed

    public void addRowToJTable(){
        
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        Object rowData[] = new Object[6];
        
        int rowCount = model.getRowCount();
        for (int j = rowCount - 1; j >= 0; j--) {
            model.removeRow(j);
        }
        
        for (int i = 0; i < listaPelicula.size(); i++) {
            rowData[0] = listaPelicula.get(i).getidPelicula();
            rowData[1] = listaPelicula.get(i).getTitulo();
            rowData[2] = listaPelicula.get(i).getGenero();
            rowData[3] = listaPelicula.get(i).getClasificacion();
            rowData[4] = listaPelicula.get(i).getFormato();
            rowData[5] = listaPelicula.get(i).getDescripcion();
            model.addRow(rowData);
        }
        
        
    
    }
